def concatenate(str1, str2):
    """
    Concatenate two strings.
    
    Args:
        str1: First string
        str2: Second string
        
    Returns:
        The concatenation of str1 and str2
    """
    return str1 + str2

def reverse(s):
    """
    Reverse a string.
    
    Args:
        s: String to reverse
        
    Returns:
        The reversed string
    """
    return s[::-1]

